    private void btnRetrieveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetrieveActionPerformed
        try {
            // TODO add your handling code here:
            pst = con.prepareStatement("select Address, Contact_no, email from librarian where Name = ?");
            String name = txtName.getText();
                pst.setString(1, name);
                ResultSet rs = pst.executeQuery();
                if(rs.next()==false){
                    JOptionPane.showMessageDialog(this, "Sorry Record Not Found");
                    txtAddress.setText("");
                    txtContact.setText("");
                    txtEmail.setText("");
                
                    txtName.requestFocus();
                }
                else{
                    txtAddress.setText(rs.getString("Address"));
                    txtContact.setText(rs.getString("Contact_no"));
                    txtEmail.setText(rs.getString("email"));
                }
            
        } catch (SQLException ex) {
            Logger.getLogger(librarianInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnRetrieveActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText(); 
        
        String address = txtAddress.getText();
        String contact = txtContact.getText();
        String email = txtEmail.getText();
        
        try {
            pst = con.prepareStatement("update librarian set Address = ?, Contact_no = ?, email = ? where Name = ?");
            pst.setString(1,address);
            pst.setString(2,contact);
            pst.setString(3,email);
            pst.setString(4,name);
            int k = pst.executeUpdate();
            
            if (k==1){
                JOptionPane.showMessageDialog(this, "Librarian Edited");
                
                LibrarianLoad();
            }else{
                JOptionPane.showMessageDialog(this, "Error");
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(librarianInterface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

    
    
